function getLanguage() {
  const savedLang = localStorage.getItem('wrc-language');
  if (savedLang && (savedLang === 'hu' || savedLang === 'en')) {
    return savedLang;
  }
  return 'en';
}

function t(key, params = {}) {
  const lang = getLanguage();
  let text = TRANSLATIONS[lang]?.[key] || TRANSLATIONS['en'][key] || key;
  
  for (const param in params) {
    text = text.replace(`{${param}}`, params[param]);
  }
  return text;
}

function setLanguage(lang) {
  if (lang === 'hu' || lang === 'en') {
    localStorage.setItem('wrc-language', lang);
    window.dispatchEvent(new CustomEvent('wrc-language-changed', { detail: { language: lang } }));
  }
}

window.TRANSLATIONS = TRANSLATIONS;
window.t = t;
window.setLanguage = setLanguage;
window.getLanguage = getLanguage;